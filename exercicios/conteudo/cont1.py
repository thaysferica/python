nu1 = input("digite alguma coisa")
print(nu1.isnumeric())
print (nu1.isalpha())
#isnumeric é para números
#isalpha é para letras
print (nu1.isalnum())
#isalnum é tanto para letras quanto para números
print (nu1.isupper())
#isupper é para letra maiscula
print (nu1.isdecimal())
#isdecimal é para número decimal
print (nu1.islower())
#islower para letra minuscula
print (nu1.isspace())
#verifica se tem espaço
